// Şehir seçimi değiştikçe ilçeleri güncelleyin
function ilceSecimiGuncelle() {
    const secilenSehirId = sehirSelect.value;
    // Önce tüm ilçe seçeneklerini temizleyin
    ilceSelect.innerHTML = "<option value='0'>İlçe Seçin</option>";

    // Seçilen şehre ait ilçeleri bulun
    const ilceSecenekleri = ilceler.filter((ilce) => ilce.sehirId === secilenSehirId);

    // İlçeleri seçeneklere ekleyin
    ilceSecenekleri.forEach((ilce) => {
        const option = document.createElement("option");
        option.value = ilce.ad;
        option.text = ilce.ad;
        ilceSelect.appendChild(option);
    });
}
